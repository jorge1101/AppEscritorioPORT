    private void btnVerReservasFechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerReservasFechaActionPerformed
try {
    DefaultTableModel modelo = new DefaultTableModel();
    this.jTable1.setModel(modelo);

    String url = "jdbc:oracle:thin:@localhost:1521/XE";
    cn = DriverManager.getConnection(url, "Portafolio", "duoc");
    s = cn.createStatement();

    // Obtener la fecha seleccionada del componente jCalendarReservas
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    String fechaSeleccionada = dateFormat.format(jCalendarDepto.getDate());

    // Obtener la ID del departamento
    String idDepartamento = jTextFieldCodigoDepto.getText();

    // Modificar la consulta para filtrar por el rango de fechas y la ID del departamento
    String consulta = "SELECT * FROM reserva WHERE TO_DATE('" + fechaSeleccionada + "', 'YYYY-MM-DD') BETWEEN fec_ini AND fec_ter AND id_departamentopago = '" + idDepartamento + "'";
    rs = s.executeQuery(consulta);

    ResultSetMetaData rsMd = rs.getMetaData();
    int cantidadColumnas = rsMd.getColumnCount();

    // Establecer las columnas del modelo
    for (int i = 1; i <= cantidadColumnas; i++) {
        modelo.addColumn(rsMd.getColumnLabel(i));
    }

    // Agregar las filas al modelo
    while (rs.next()) {
        Object[] fila = new Object[cantidadColumnas];
        for (int i = 0; i < cantidadColumnas; i++) {
            fila[i] = rs.getObject(i + 1);
        }
        modelo.addRow(fila);
    }

    rs.close();
    cn.close();
} catch (Exception ex) {
    ex.printStackTrace();
}
    }//GEN-LAST:event_btnVerReservasFechaActionPerformed

