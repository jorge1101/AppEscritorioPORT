    private void btnDescargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescargarActionPerformed
 try {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Guardar como CSV");
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            FileWriter fw = new FileWriter(fileToSave + ".csv");

            // Escribir encabezados
            for (int i = 0; i < modelo.getColumnCount(); i++) {
                fw.append(modelo.getColumnName(i));
                if (i < modelo.getColumnCount() - 1) {
                    fw.append(",");
                } else {
                    fw.append("\n");
                }
            }

            // Escribir datos
            for (int i = 0; i < modelo.getRowCount(); i++) {
                for (int j = 0; j < modelo.getColumnCount(); j++) {
                    fw.append(modelo.getValueAt(i, j).toString());
                    if (j < modelo.getColumnCount() - 1) {
                        fw.append(",");
                    } else {
                        fw.append("\n");
                    }
                }
            }

            fw.close();
            System.out.println("Archivo CSV guardado con éxito.");
        }
    } catch (IOException ex) {
        ex.printStackTrace();
    }       
    }//GEN-LAST:event_btnDescargarActionPerformed

