    private void jButtonfiltroDeptoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonfiltroDeptoActionPerformed
        // TODO add your handling code here:
    Connection conn = null;
    PreparedStatement pstmt = null;
try {
            //Para establecer el modelo al JTable
            DefaultTableModel modelo = new DefaultTableModel();
            this.jTable2.setModel(modelo);
            //Para conectarnos a nuestra base de datos
            String url = "jdbc:oracle:thin:@localhost:1521/XE";
            // Establecemos los valores de cadena de conexión, usuario y contraseña
            cn = DriverManager.getConnection(url, "Portafolio", "duoc");
            //Para ejecutar la consulta
            s = cn.createStatement();
            //Ejecutamos la consulta y los datos lo almacenamos en un ResultSet
             rs = s.executeQuery("select d.nro_arrendado,d.servicio_asociado as servicio,d.descripcion_cabania as descripcion,SUM(d.valor_noche) as valor_total,c.nombre_comuna as comuna from departamento d join comuna c on d.id_comuna=c.id_comuna where nro_arrendado='"+jTextFieldFiltroDepto.getText()+"' GROUP BY d.nro_arrendado, d.servicio_asociado, d.descripcion_cabania, c.nombre_comuna");
            //Obteniendo la informacion de las columnas que estan siendo consultadas
            ResultSetMetaData rsMd = rs.getMetaData();
            //La cantidad de columnas que tiene la consulta
            int cantidadColumnas = rsMd.getColumnCount();
            //Establecer como cabezeras el nombre de las columnas
            for (int i = 1; i <= cantidadColumnas; i++) {
             modelo.addColumn(rsMd.getColumnLabel(i));
            }
            //Creando las filas para el JTable
            while (rs.next()) {
             Object[] fila = new Object[cantidadColumnas];
             for (int i = 0; i < cantidadColumnas; i++) {
               fila[i]=rs.getObject(i+1);
             }
             modelo.addRow(fila);
            }
            rs.close();
            cn.close();
       } catch (Exception ex) {
        ex.printStackTrace();
       }
    }//GEN-LAST:event_jButtonfiltroDeptoActionPerformed

