    private void jButtonReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReporteActionPerformed
    Connection conn = null;
    PreparedStatement pstmt = null;

    try {
        // Configura la conexión a la base de datos Oracle
        conn = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:XE", "Portafolio", "duoc");
        String consultaSQL = "SELECT * from lista_pagos";

        PreparedStatement stmt = conn.prepareStatement(consultaSQL);

        // Ejecuta la consulta y obtiene los resultados
        ResultSet resultSet = stmt.executeQuery();

        // Crear el documento PDF
        Document documento = new Document();

        // Nombre del archivo PDF
        String nombreArchivo = "C:\\Users\\flame\\Downloads\\Datos Estadisticas .pdf";

        // Crear el archivo PDF
        PdfWriter.getInstance(documento, new FileOutputStream(nombreArchivo));

        // Abre el documento para comenzar a escribir
        documento.open();

        // Personalizar el contenido del PDF
        // Crear una lista numerada o con viñetas
        List lista = new List(List.UNORDERED);

        double sumaMonto = 0;

        while (resultSet.next()) {
            String id = resultSet.getString("id_transaccion");
            String correo = resultSet.getString("correo_transaccion");
            String monto = resultSet.getString("monto_transaccion");
            String tipoMoneda = resultSet.getString("tipo_moneda");
            String fechaPago = resultSet.getString("fecha_pago");

            // Agregar los datos al PDF
            sumaMonto += Double.parseDouble(monto);
            lista.add("id numero de transaccion: " + id);
            lista.add("correo del usuario: " + correo);
            lista.add("monto de la transaccion: " + monto);
            lista.add("tipo de moneda de la transaccion: " + tipoMoneda);
            lista.add("fecha de pago de la transaccion: " + fechaPago);
            lista.add(" "); // Espacio entre elementos de la lista
        }

        // Agregar la lista al documento
        documento.add(lista);

        // Agregar la suma total al PDF
        documento.add(new Paragraph("Suma total de montos: " + sumaMonto));

        // Cierra el documento
        documento.close();

        // Cierra la conexión y libera recursos
        conn.close();

        System.out.println("Archivo PDF creado con éxito.");
        System.out.println("Ruta del archivo PDF: " + nombreArchivo);
    } catch (Exception e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButtonReporteActionPerformed

